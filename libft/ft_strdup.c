#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t		i;
	size_t		size;
	char		*dest;

	i = 0;
	size = ft_strlen(s);
	if (!s)
		return (NULL);
	dest = malloc(size + 1);
	if (!dest)
		return (NULL);
	while (i < size)
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
